# There are two types of translators in Python: compilers and interpreters. 

# Compliler:
# A compiler translates the entire source code into machine code before execution.
# 
# Interpreter:
# An interpreter translates the source code line by line during execution. 

# Python is primarily an interpreted language, but it also uses a compiler to convert the source code into bytecode, 
# which is then executed by the Python virtual machine (PVM). This allows for platform independence and easier debugging.
